public class Usuario {
    
    private String rutUsuario;
    private String nombreUsuario;
    private String apellidoUsuario;
    private String direccionUsuario;
    private String comunaUsuario;
    private String sueldoUsuario;
    private int tipoUsuario;

    /**
     * @return the rutUsuario
     */
    public String getRutUsuario() {
        return rutUsuario;
    }

    /**
     * @param rutUsuario the rutUsuario to set
     */
    public void setRutUsuario(String rutUsuario) {
        this.rutUsuario = rutUsuario;
    }

    /**
     * @return the nombreUsuario
     */
    public String getNombreUsuario() {
        return nombreUsuario;
    }

    /**
     * @param nombreUsuario the nombreUsuario to set
     */
    public void setNombreUsuario(String nombreUsuario) {
        this.nombreUsuario = nombreUsuario;
    }

    /**
     * @return the apellidoUsuario
     */
    public String getApellidoUsuario() {
        return apellidoUsuario;
    }

    /**
     * @param apellidoUsuario the apellidoUsuario to set
     */
    public void setApellidoUsuario(String apellidoUsuario) {
        this.apellidoUsuario = apellidoUsuario;
    }

    /**
     * @return the direccionUsuario
     */
    public String getDireccionUsuario() {
        return direccionUsuario;
    }

    /**
     * @param direccionUsuario the direccionUsuario to set
     */
    public void setDireccionUsuario(String direccionUsuario) {
        this.direccionUsuario = direccionUsuario;
    }

    /**
     * @return the comunaUsuario
     */
    public String getComunaUsuario() {
        return comunaUsuario;
    }

    /**
     * @param comunaUsuario the comunaUsuario to set
     */
    public void setComunaUsuario(String comunaUsuario) {
        this.comunaUsuario = comunaUsuario;
    }

    /**
     * @return the sueldoUsuario
     */
    public String getSueldoUsuario() {
        return sueldoUsuario;
    }

    /**
     * @param sueldoUsuario the sueldoUsuario to set
     */
    public void setSueldoUsuario(String sueldoUsuario) {
        this.sueldoUsuario = sueldoUsuario;
    }

    /**
     * @return the tipoUsuario
     */
    public int getTipoUsuario() {
        return tipoUsuario;
    }

    /**
     * @param tipoUsuario the tipoUsuario to set
     */
    public void setTipoUsuario(int tipoUsuario) {
        this.tipoUsuario = tipoUsuario;
    }
    
    
    
}
